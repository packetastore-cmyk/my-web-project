export function detectPlatform(url) {
  if (!url || typeof url !== "string") return null;
  const u = url.toLowerCase().trim();

  if (u.includes("youtube.com") || u.includes("youtu.be"))
    return { id: "youtube", name: "YouTube", color: "#FF0000" };
  if (u.includes("tiktok.com"))
    return { id: "tiktok", name: "TikTok", color: "#000000" };
  if (u.includes("instagram.com"))
    return { id: "instagram", name: "Instagram", color: "#E4405F" };
  if (
    u.includes("facebook.com") ||
    u.includes("fb.watch") ||
    u.includes("fb.com")
  )
    return { id: "facebook", name: "Facebook", color: "#1877F2" };
  if (u.includes("twitter.com") || u.includes("x.com"))
    return { id: "twitter", name: "X (Twitter)", color: "#000000" };
  if (u.includes("pinterest."))
    return { id: "pinterest", name: "Pinterest", color: "#E60023" };
  if (u.includes("reddit.com"))
    return { id: "reddit", name: "Reddit", color: "#FF4500" };
  if (u.includes("vimeo.com"))
    return { id: "vimeo", name: "Vimeo", color: "#1AB7EA" };
  if (u.includes("dailymotion.com"))
    return { id: "dailymotion", name: "Dailymotion", color: "#0066DC" };
  if (u.includes("twitch.tv"))
    return { id: "twitch", name: "Twitch", color: "#9146FF" };
  if (u.includes("soundcloud.com"))
    return { id: "soundcloud", name: "SoundCloud", color: "#FF5500" };
  if (u.includes("t.me") || u.includes("telegram."))
    return { id: "telegram", name: "Telegram", color: "#0088CC" };
  if (u.includes("snapchat.com"))
    return { id: "snapchat", name: "Snapchat", color: "#FFFC00" };
  if (u.includes("linkedin.com"))
    return { id: "linkedin", name: "LinkedIn", color: "#0A66C2" };

  try {
    new URL(url);
    return { id: "generic", name: "Web", color: "#6B7280" };
  } catch {
    return null;
  }
}
